package com.ejemplo.botones.util;

import android.widget.ImageButton;

public class Mision {
	private int mision;
	private int tiempo;
	private ImageButton boton;

	public int getTiempo() {
		return tiempo;
	}

	public void setTiempo(int tiempo) {
		this.tiempo = tiempo;
	}

	public ImageButton getBoton() {
		return boton;
	}

	public void setBoton(ImageButton boton) {
		this.boton = boton;
	}

	public int getMision() {
		return mision;
	}

	public void setMision(int mision) {
		this.mision = mision;
	}

}
